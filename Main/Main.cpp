void Main :: Start()
{
	m_ZEngineWindow.Create(800, 600, "My Window");
	ZEngine::CVector2D<int> pos(0, 0);

	while (1)
	{
		m_ZEngineWindow.Process();
		m_ZEngineWindow.SetPosition(pos.x, pos.y);
	}
}

