#include "ZColor.h"

//-----------------------------------------------------------
//
//-----------------------------------------------------------
ZColor::ZColor()
{
	r = 0x00;
	g = 0x00;
	b = 0x00;
	a = 0xFF;
}

//-----------------------------------------------------------
//
//-----------------------------------------------------------
ZColor::ZColor(int p_nHexaColor)
{
	r = ((p_nHexaColor >> 24) & 0xFF);
	g = ((p_nHexaColor >> 16) & 0xFF);
	b = ((p_nHexaColor >> 8) & 0xFF);
	a = ((p_nHexaColor)& 0xFF);
}

//-----------------------------------------------------------
//
//-----------------------------------------------------------
void ZColor::operator=(const ZColor& p_Color)
{
	r = p_Color.r;
	g = p_Color.g;
	b = p_Color.b;
	a = p_Color.a;
}

//-----------------------------------------------------------
//
//-----------------------------------------------------------
void ZColor::operator=(int p_nHexaColor)
{
	r = ((p_nHexaColor >> 24) & 0xFF);
	g = ((p_nHexaColor >> 16) & 0xFF);
	b = ((p_nHexaColor >> 8) & 0xFF);
	a = ((p_nHexaColor)& 0xFF);
}